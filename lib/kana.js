// 直前の1文字を小文字/大文字に変換する。対応しない文字はそのまま返す。
export function applyKogakiToText(text) {
  if (!text) return text;
  const last = text[text.length - 1];
  const conv = KOGAKI[last];
  if (!conv) return text;
  return text.slice(0, -1) + conv;
}

// 直前の1文字に濁点/半濁点を付ける。付けられない文字はそのまま返す。
export function applyMarkToText(text, kind) {
  if (!text) return text;
  const map = kind === "handakuten" ? HANDAKUTEN : DAKUTEN;
  const last = text[text.length - 1];
  const conv = map[last];
  if (!conv) return text;
  return text.slice(0, -1) + conv;
}

// お題と入力を比べる。同じ字数なら違う位置を返す。
// 返り値: { correct: bool, sameLength: bool, wrong: number[] }
export function compareToTarget(value, target) {
  if (value === target) return { correct: true, sameLength: true, wrong: [] };
  if (value.length === target.length) {
    const wrong = [];
    for (let i = 0; i < value.length; i++) {
      if (value[i] !== target[i]) wrong.push(i);
    }
    return { correct: false, sameLength: true, wrong };
  }
  return { correct: false, sameLength: false, wrong: [] };
}
